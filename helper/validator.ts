import Joi from 'joi'

const brandSchema = Joi.object({
    name: Joi.string()
        .alphanum()
        .min(3)
        .max(30)
        .required(),
    password: Joi.string()
        .pattern(new RegExp('^[a-zA-Z0-9]{3,30}$')),

    email: Joi.string()
        .email({ minDomainSegments: 2 }),
});

export  function ValidateBrand(brand: any){
    return brandSchema.validate(brand)
}